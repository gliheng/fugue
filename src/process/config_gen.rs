    #[test]
    fn test_generate_dispatch_mjs_empty() {
        let apps: Vec<App> = vec![];
        let refs: Vec<&App> = apps.iter().collect();
        let code = generate_dispatch_mjs(&refs);
        assert!(code.contains("const routes = {\n};"));
        assert!(code.contains("export default"));
    }

