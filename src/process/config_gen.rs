    #[test]
    fn test_generate_dispatch_config_skips_missing_artifacts() {
        let dir = std::env::temp_dir().join("fugue-test-artifact-skip");
        let _ = std::fs::remove_dir_all(&dir);
        std::fs::create_dir_all(&dir).unwrap();

        let apps = vec![make_app("missing-worker", "worker", "running")];
        generate_dispatch_config(&apps, &dir, 8080).unwrap();

        let config = std::fs::read_to_string(dir.join("config.capnp")).unwrap();
        assert!(!config.contains("missingWorker"));

        let dispatch = std::fs::read_to_string(dir.join("dispatch.mjs")).unwrap();
        assert!(!dispatch.contains("missing-worker"));
        assert!(dispatch.contains("export default"));

        let _ = std::fs::remove_dir_all(&dir);
    }

