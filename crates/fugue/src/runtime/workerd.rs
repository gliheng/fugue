fn generate_vite_entry_worker(workerd_func_dir: &Path) -> Result<()> {
    let entry_code = r#"// Auto-generated by fugue — do not edit
import assets from "static-assets.mjs";

function serveAsset(asset) {
  return new Response(
    Uint8Array.from(atob(asset.data), c => c.charCodeAt(0)),
    {
      headers: {
        "Content-Type": asset.mime,
        "Cache-Control": "public, max-age=31536000, immutable",
      },
    }
  );
}

export default {
  async fetch(request, env, ctx) {
    const url = new URL(request.url);
    const pathname = url.pathname;

    // API routes go to the Vite worker bundle
    if (pathname.startsWith("/api/")) {
      try {
        return await env.SSR.fetch(request);
      } catch (err) {
        return new Response("Worker Error: " + (err instanceof Error ? err.message : String(err)), {
          status: 500,
          headers: { "Content-Type": "text/plain" },
        });
      }
    }

    // Exact static asset match
    const asset = assets.get(pathname);
    if (asset) {
      return serveAsset(asset);
    }

    // SPA fallback: serve index.html for all non-API routes
    const indexAsset = assets.get("/index.html");
    if (indexAsset) {
      return serveAsset(indexAsset);
    }

    return new Response("Not Found", { status: 404 });
  },
};
"#;
    let entry_path = workerd_func_dir.join("entry.mjs");
    std::fs::write(&entry_path, entry_code)?;
    Ok(())
}

